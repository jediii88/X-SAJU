with open('/home/node/.openclaw/workspace/X-SAJU_MASTER.html', 'r', encoding='utf-8') as f:
    html = f.read()

import re

# ──────────────────────────────────────────────
# 1. 괄호 안 한자 제거: "대운(大運)" / "대운(대운)" 등 모두 → "대운"
#    단, JS 오브젝트 키('甲':'wood' 등)는 건드리지 않는다
# ──────────────────────────────────────────────

# 한자 유니코드 범위: 4E00-9FFF
HANJA_PAT = r'[\u4e00-\u9fff]+'

# 패턴: 한글단어(한자) → 한글단어  (ex: 대운(大運) → 대운)
# 패턴: 한글단어(한자(한글)) → 한글단어  (ex: 대운(大運(대운)) → 대운)
replaced = 0

def remove_hanja_paren(m):
    global replaced
    replaced += 1
    return m.group(1)  # 괄호 앞 한글만 반환

# (한글+)(한자) 패턴
html2 = re.sub(r'([\uac00-\ud7a3]+)\([' + '\u4e00-\u9fff' + r']+\)', remove_hanja_paren, html)
print(f'괄호 한자 제거: {replaced}개')

# ──────────────────────────────────────────────
# 2. buildCurrentPeriodSummary 안의 label 텍스트 수정
# ──────────────────────────────────────────────
