# ─── 1단계: 한자(한글(한자)) 이중괄호 패턴 제거 → 한글만 남기기
# 예: 大運(대운(大運)) → 대운, 대운(大運(대운)) → 대운
# 패턴: 한글어(漢字(한글어)) or 漢字(한글어(漢字))
double_bracket = re.compile(r'[가-힣]+\([^\)]+\([^\)]*\)\)')
def simplify_double(m):
    # 맨 앞 한글만 추출
    text = m.group(0)
    kor = re.match(r'([가-힣]+)', text)
    return kor.group(1) if kor else text

html = double_bracket.sub(simplify_double, html)

# 추가: 漢字(한글(漢字)) 패턴
double_bracket2 = re.compile(r'[\u4e00-\u9fff]+\(([가-힣]+)\([\u4e00-\u9fff]+\)\)')
html = double_bracket2.sub(lambda m: m.group(1), html)

# ─── 2단계: 한자(한글) 패턴 → 한글만 (공간 없을 때)
# 단, 일부 중요 전문 용어는 남길 수 있으나 일단 모두 한글로
hanja_kor_pattern = re.compile(r'([\u4e00-\u9fff]{1,6})\(([가-힣]{1,8})\)')

def replace_hanja_kor(m):
    hanja = m.group(1)
    kor = m.group(2)
    return kor  # 한글만

html = hanja_kor_pattern.sub(replace_hanja_kor, html)

