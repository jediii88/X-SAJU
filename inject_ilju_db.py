def get_relation(s_oh, b_oh):
    if s_oh == b_oh:
        return "비화(比和) — 같은 오행이 겹쳐 에너지가 응집"
    if (s_oh, b_oh) in GENERATES:
        return f"일간이 일지를 생(生)하는 구조 — {OH_KR[s_oh]}이 {OH_KR[b_oh]}에 에너지를 공급"
    if (b_oh, s_oh) in GENERATES:
        return f"일지가 일간을 생(生)하는 구조 — {OH_KR[b_oh]}이 {OH_KR[s_oh]}를 강화"
    if (s_oh, b_oh) in CONTROLS:
        return f"일간이 일지를 극(剋)하는 구조 — {OH_KR[s_oh]}이 {OH_KR[b_oh]}를 제압, 강한 자기주장"
    if (b_oh, s_oh) in CONTROLS:
        return f"일지가 일간을 극(剋)하는 구조 — 환경이 나를 압박, 역경 속 성장"
    return "특수한 에너지 구조"

