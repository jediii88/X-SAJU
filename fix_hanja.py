for hanja, hangul in HANJA_MAP.items():
    # 이미 병기된 경우 스킵: 한자(한글) 또는 한자（한글）
    pattern = re.compile(re.escape(hanja) + r'(?!\uff08|\(|' + re.escape(hangul) + ')')
    new = f'{hanja}({hangul})'
    result = pattern.sub(new, html)
    if result != html:
        n = len(pattern.findall(html))
        count += n
        html = result

print(f'교체 완료: {count}건')

